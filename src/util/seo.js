// Function to get SEO data for a specific page
export const getSEOData = (page, subPage = null) => {
  if (subPage && seoConfig.serviceDetails && seoConfig.serviceDetails[subPage]) {
    return seoConfig.serviceDetails[subPage];
  }
  
  return seoConfig[page] || seoConfig.default;
};

// Function to generate JSON-LD structured data
export const generateStructuredData = (data) => {
  if (!data.structuredData) return null;
  
  return JSON.stringify(data.structuredData);
};

// Common Open Graph and Twitter Card data
export const getOpenGraphData = (seoData) => ({
  'og:type': 'website',
  'og:title': seoData.title,
  'og:description': seoData.description,
  'og:url': seoData.url,
  'og:image': seoData.image || seoConfig.default.image,
  'og:site_name': 'NeuroraLynx AI',
  'og:locale': 'en_US',
  'twitter:card': 'summary_large_image',
  'twitter:title': seoData.title,
  'twitter:description': seoData.description,
  'twitter:image': seoData.image || seoConfig.default.image,
  'twitter:url': seoData.url
});

// Function to generate complete meta tags
export const generateMetaTags = (page, subPage = null) => {
  const seoData = getSEOData(page, subPage);
  const ogData = getOpenGraphData(seoData);
  
  return {
    title: seoData.title,
    meta: [
      { name: 'description', content: seoData.description },
      { name: 'keywords', content: seoData.keywords },
      { name: 'robots', content: 'index, follow, max-snippet:-1, max-image-preview:large, max-video-preview:-1' },
      { name: 'author', content: 'NeuroraLynx AI' },
      { name: 'language', content: 'English' },
      { name: 'revisit-after', content: '7 days' },
      { name: 'rating', content: 'general' },
      { name: 'distribution', content: 'global' },
      // Open Graph tags
      ...Object.entries(ogData).map(([property, content]) => ({ 
        property, 
        content 
      }))
    ],
    link: [
      { rel: 'canonical', href: seoData.url }
    ],
    script: seoData.structuredData ? [
      {
        type: 'application/ld+json',
        innerHTML: generateStructuredData(seoData)
      }
    ] : []
  };
}; 